			Se realizo una página web sencilla en php usando hojas de estilo y javascript</br></br>	
			
La pagina cuenta con una autentificación de php para un usuario, no está conectada a ninguna base de datos por lo que la autentificación es estática.
</br></br>En la pagina se pueden apreciar las fotografías de varios animales con una pequeña leyenda bajo las imágenes, estas imágenes al pasar el cursor sobre ellas se agrandaran un poco y al hacer click sobre ellas se expandirán a pantalla completa, para regresar a la pantalla anterior tenemos que presionar la tecla “X” o  “Esc” también podemos hacer click con el ratón en alguna parte de la pagina para cerrar la imagen. 
</br></br>Las hojas de estilo es un archivo con extensión .css que contiene formatos para la visualización de la pagina, tales como tipo de fuente, tamaño de fuente, color de fuente, párrafos, tipo de tabla, fondo de páginas, localización de objetos entre otras etiquetas que componen una pagina web.
</br></br>La ventaja de las hojas de estilo es que no tenemos que darle formato a todas las partes de las diferentes paginas, solo tenemos que mandar llamar la hoja de estilo que contiene las especificaciones en cada una de las diferentes paginas para tener el mismo formato en todas las paginas.
Otra ventaja es que si deseamos modificar cierta cosa, con tan solo modificar la hoja de estilo se verá reflejado en todas las paginas.
</br></br>	
Las hojas javascript es un lenguaje de programación del lado del cliente, este tienen la misma funcionalidad que las hojas de estilo, con la diferencia que estas contienen scripts en lugar de formatos.
</br></br>Un script es un archivo almacenado en un texto plano, el script se ejecuta por lotes realizando funciones sencillas.
</br></br>En el proyecto se utiliza para poder acceder a la caja de luz (Lightbox), este scripts al hacer click en una imagen tomara toda la pantalla con un fondo transparente y opaco en el centro se apreciara la imagen con su tamaño original ampliada significativamente, se incluye la posibilidad de pasar de imagen en imagen sin necesidad de salir de la vista de la imagen para posteriormente abrir otra vista de imagen. 
Cuenta con otras dos paginas independientes al index, están también mandan llamar a las hojas de estilo y los scripts, en la pagina mail.php se pueden apreciar los datos del desarrollador y datos de la materia se incluye una sección para enviar un correo electrónico, la otra pagina se incluye este documento que describe las funciones de la pagina web.
